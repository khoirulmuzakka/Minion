def _problem02_bounds(ndim: int):
    lb = np.zeros(ndim)
    for idx in range(3, ndim):
        lb[idx] = -4 - 0.25 * int((idx - 4) / 3)

    ub = np.zeros(ndim)
    ub[0] = ub[1] = 4.0
    ub[2] = np.pi
    for idx in range(3, ndim):
        ub[idx] = 4 + 0.25 * int((idx - 4) / 3)
    return lb, ub


def _problem05_bounds(ndim: int):
    lb = -1.0 * np.ones(ndim)
    lb[:3] = 0.0
    ub = np.zeros(ndim)
    ub[0] = ub[1] = 4.0
    ub[2] = np.pi
    for idx in range(3, ndim):
        ub[idx] = 4 + 0.25 * int((idx - 4) / 3)
    return lb, ub


def _problem06_bounds(ndim: int):
    lb = -1.0 * np.ones(ndim)
    lb[:3] = 0.0
    ub = np.zeros(ndim)
    ub[0] = ub[1] = 4.0
    ub[2] = np.pi
    for idx in range(3, ndim, 3):
        ub[idx] = 4 + 0.25 * int((1 - 4) / 3)
        if idx + 1 < ndim:
            ub[idx + 1] = 4 + 0.25 * int((2 - 4) / 3)
        if idx + 2 < ndim:
            ub[idx + 2] = 4 + 0.25 * int((3 - 4) / 3)
    return lb, ub


