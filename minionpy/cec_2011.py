class CEC2011:
    """
    Python wrapper around the C++ implementation of the CEC2011 benchmark problems.
    """

    def __init__(self, function_number, max_workers=1):
        if function_number not in PROBLEM_METADATA:
            raise Exception("Function number must be between 1 and 22.")
        self.num = function_number
        self.dimension, lb, ub = PROBLEM_METADATA[function_number]
        self.lb = lb.tolist()
        self.ub = ub.tolist()
        self._cpp = cppCEC2011Functions(function_number, self.dimension)

    def evaluate(self, xs):
        arr = np.asarray(xs, dtype=float)
        if arr.ndim == 1:
            arr = arr.reshape(1, -1)
        if arr.shape[1] != self.dimension:
            raise ValueError(f"Expected dimension {self.dimension}, got {arr.shape[1]}")
        data = arr.tolist()
        return np.array(self._cpp(data))

    def getBounds(self):
        return list(zip(self.lb, self.ub))
